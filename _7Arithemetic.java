public class _7Arithemetic {
    public static void main(String[] args) {
        int a=6;
        int b=4;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        double c=6.5;
        double d=7.4;
        System.out.println(c+d);
        System.out.println(c-d);
        System.out.println(c*d);
        System.out.println(c/d);
        System.out.println(c%d);

    }
    
}
